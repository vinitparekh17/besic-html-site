function change() {
    var opt = document.getElementById("menu");

    if(opt.value == "2") {
        location.href = "./assets/drump/index.html"
    }
    if(opt.value == "1") {
        location.href = "./assets/dicegame/index.html"
    }
    if(opt.value == "3") {
        location.href = "./assets/digital clock/index.html"
    }
}